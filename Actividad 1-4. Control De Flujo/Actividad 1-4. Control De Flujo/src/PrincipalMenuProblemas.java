  private void btnProblema1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProblema1ActionPerformed
    /*PROBLEMA 1
    - Programa dedicado a calcular el área de un triángulo.
    */
    
    // Declaracion de variables:
    double p, a , b, c, area;
    
      // Inicio del programa:
      a = Double.parseDouble(JOptionPane.showInputDialog(this, "Introduce el valor del lado del triangulo: "));

      b = Double.parseDouble(JOptionPane.showInputDialog(this, "Introduce el valor del segundo lado del triangulo:" ));

      c = Double.parseDouble(JOptionPane.showInputDialog(this, "Introduce el valor del tercer lado del triangulo:"));

      // Procesar los datos:
      p = (a + b + c)/2; // Semiperimetro
      area = Math.sqrt(p * (p-a) * (p-b) * (p-c)); // Area del Triangulo
      
      // Salida de datos:
      JOptionPane.showMessageDialog(this, "El área del triángulo es: " + area);
//FIN DE PROBLEMA 1
  }//GEN-LAST:event_btnProblema1ActionPerformed


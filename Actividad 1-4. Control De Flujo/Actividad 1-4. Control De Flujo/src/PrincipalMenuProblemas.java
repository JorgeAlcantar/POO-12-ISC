  private void btnProblema4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProblema4ActionPerformed
    /* PROBLEMA 4
     - Programa dedicado a invertir un número de 5 cifras.
    */
    
    // Declaracion de variables:
    int numero, digito, numInvertido;
    numInvertido = 0;
      
      // Inicio del programa:
      numero = Integer.parseInt(JOptionPane.showInputDialog(this, "***NÚMERO INVERTIDO***\n" + "Introduce el número entero de 5 cifras: "));
      
      int numOg = numero; // Guardar el valor de numero dado
      
      /* Procesar datos:
       - Estructura condicional (doble)
      */
      
      if (numero >= 10000 && numero <100000){
        //Estructura cíclica (mientras)
        while(numero != 0){
          // Procesar los datos:
          digito = numero % 10;
          numInvertido = numInvertido * 10 + digito;
          numero = numero / 10;
        }
        
        // Salida del programa:
        JOptionPane.showMessageDialog(this, "Número original: " + numOg + "\nNúmero invertido: " + numInvertido);
      }else{
        JOptionPane.showMessageDialog(this, "Dato incorrecto");
      }
//FIN DEL PROBLEMA 4
  }//GEN-LAST:event_btnProblema4ActionPerformed

